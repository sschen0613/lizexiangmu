package com.yr.lize.pojo;

public class SystemPosition {
    private Integer position_Id;

    private String position_Name;

    private Integer position_Power;
    
    private Integer position_details;
    
    private Integer position_storage;

	public Integer getPosition_storage() {
		return position_storage;
	}

	public void setPosition_storage(Integer position_storage) {
		this.position_storage = position_storage;
	}

	public Integer getPosition_details() {
		return position_details;
	}

	public void setPosition_details(Integer position_details) {
		this.position_details = position_details;
	}

	public Integer getPosition_Id() {
		return position_Id;
	}

	public void setPosition_Id(Integer position_Id) {
		this.position_Id = position_Id;
	}

	public String getPosition_Name() {
		return position_Name;
	}

	public void setPosition_Name(String position_Name) {
		this.position_Name = position_Name;
	}

	public Integer getPosition_Power() {
		return position_Power;
	}

	public void setPosition_Power(Integer position_Power) {
		this.position_Power = position_Power;
	}

  
}