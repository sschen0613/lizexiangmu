package com.yr.lize.pojo;

public class ApprovalCondition {
	
	private Integer condition_id;
	
	private Integer approval_id;
	
	private Integer company_id;
	
	private Integer condition;

	public Integer getCondition_id() {
		return condition_id;
	}

	public void setCondition_id(Integer condition_id) {
		this.condition_id = condition_id;
	}

	public Integer getApproval_id() {
		return approval_id;
	}

	public void setApproval_id(Integer approval_id) {
		this.approval_id = approval_id;
	}

	public Integer getCompany_id() {
		return company_id;
	}

	public void setCompany_id(Integer company_id) {
		this.company_id = company_id;
	}

	public Integer getCondition() {
		return condition;
	}

	public void setCondition(Integer condition) {
		this.condition = condition;
	}
	
	
	
}
